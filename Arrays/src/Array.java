public class Array {

    public static void main(String[] args) {

        //String array
        String[] stringArr = {"My","name","is","Elon"};
        for(String word: stringArr){
            System.out.println(word);
        }

        //Number array
        int[] intArr = {1,2,3,4,5,7,6,5,4};
        for(int number: intArr){
            System.out.println(number);
        }

    }
}
